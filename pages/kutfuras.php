                        <li>
                           <h4>Csak minősített anyagok beépítése</h4>
                           <p>Egy kiváló termék a minőségi alkatrészektől lesz értékes. A Madricon szolgáltatásaira is ugyanez jellemző. A magas színvonalú munka titka, többek közt abban rejlik, hogy a Madricon által használt alkatrészek és a beépítendő berendezések megbízható, ellenőrzött minőséget képviselnek.</p>   
                        </li>
                        <li>
                           <h4>Korszerű anyagok és eszközök, nagy vízhozam</h4>
                           <p>A kútfúrásnál az egyik leglényegesebb szempont, hogy a megrendelő nagy vízhozamú kutat kapjon. Ez kizárólag úgy érhető el, ha modern, korszerű megoldások kerülnek beépítésre. A Madricon szakemberei számára ez elsődleges szempont.</p>   
                        </li>
                        <li>
                           <h4>5 év garancia</h4>
                           <p>A garanciális időszak alatt a Madricon bármikor elérhető, és kész a gyors és hatékony problémamegoldásra. A vízhozamra - szakszerű üzemvitel mellett - 3 év garanciát, a kút szerkezetére (anyagára) 5 év garanciát biztosítunk</p>   
                        </li>
                        <li>
                           <h4>Komplex szolgáltatás: kivitelezés – fúrástól a csapig</h4>
                           <p>Ma már a komplex szolgáltatás jogos igénye minden megrendelőnek. Egyre többen jönnek rá, hogy pénzt és energiát spórolnak, ha a kivitelezés minden fázisa egy kézben, egy irányítás alatt van.</p>   
                        </li>
